class AlbumEntity {
  final String title;
  final String artist;
  final String album;
  final String path;
  final String duration;
  final String id;
  final String albumId;
  final String artistId;

  AlbumEntity({
    required this.title,
    required this.artist,
    required this.album,
    required this.path,
    required this.duration,
    required this.id,
    required this.albumId,
    required this.artistId,
  });
}
